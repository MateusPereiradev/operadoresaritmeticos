from math import sin, cos, tan, radians
angulo= float(input('Digite aqui um ângulo:'))
print(f'Referente ao ângulo {angulo} o seno é {sin(radians(angulo)):.2f}')
print(f'Referente ao ângulo {angulo} o cosseno é {cos(radians(angulo)):.2f}')
print(f'Referente ao ângulo {angulo} a tangente é {tan(radians(angulo)):.2f}')
print('Fim do programa!')

